package hello;

public class EqSearch{
	private int distance;
	private int minmag;

	public EqSearch(){}
	public void setDistance(int d){
		this.distance = d;
	}
	public void setMinmag(int m){
		this.minmag = m;
	}
	public int getDistance(){
		return this.distance;
	}
	public int getMinmag(){
		return this.minmag;
	}
}
